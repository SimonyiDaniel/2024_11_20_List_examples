# hónapok kiíratása
honapok = ['január', 'február','március', 'április', 'május', 'június', 'július'] 
honapok_index = 0
for honap in honapok:
    print(f'{honapok_index} {honap}')
    honapok_index += 1

print()

# index megjelenítése:
    # 0 január
    # 1 február

